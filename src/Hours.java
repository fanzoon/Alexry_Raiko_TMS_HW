public class Hours {

    public static void main(String[] args) {
        int sleepHours = 10;
        if (sleepHours < 6) {
            System.out.println("Выпейте чашечку кофе");
        } else if (sleepHours >= 10) {
            System.out.println("Примите контрасный душь");
        }else {
            System.out.println("Выпийте стакан воды");
        }


    }

}
